    public void performCalc()
    {
       
        String operation = lblMathSign.getText();
        System.out.println(operation);
        
        if (operation.equals("*"))
        {
         int answer = ranNum1 * ranNum2;    
         q3 = Integer.toString(answer); //answer into a string
        }
        
        if (operation.equals("+"))
        {
         int answer = ranNum1 + ranNum2;    
          q3 = Integer.toString(answer); //answer into a string
        }
        
        if (operation.equals("-"))
        {
         int answer = ranNum1 - ranNum2;    
         q3 = Integer.toString(answer); //answer into a string
        }

        String user = txtAnswer.getText();
       
        if (q3.equals(user)){
         lblMessage.setText("Correct!");
         
        }
        else 
            lblMessage.setText("Wrong");
                
        
        
    }
    
