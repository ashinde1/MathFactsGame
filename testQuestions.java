    public void nextQuestion ()
    {
      
        int ranNumForSign =  (int) (Math.random() * 3);
        //System.out.println(ranNumForSign);

        lblMathSign.setText(mathSigns[ranNumForSign]);

        ranNum1 = 1 + (int) (Math.random() * 10);
        q1 = Integer.toString(ranNum1);
        jTextField1.setText(q1);

        ranNum2 = 1 + (int) (Math.random() * 10);
        q2 = Integer.toString(ranNum2);
        jTextField2.setText(q2 );
        txtAnswer.setText("");
        lblMessage.setText("");
        
    }
